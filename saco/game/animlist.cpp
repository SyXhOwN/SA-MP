short GetAnimIndexByName(char *szAnimLib, char *szAnimName)
{
	short j = 0;

	if(!szAnimLib || !szAnimName) return -1;
	if(strlen(szAnimLib) + strlen(szAnimName) > 36) return -1;

	char szSearch[40];
	sprintf(szSearch, "%s:%s", szAnimLib, szAnimName);
	Util_strupr(szSearch);

	int i = 0;
	while(j != 1812) {
		if(!strcmp(szSearch, szAnimNames[i])) {
			return j + 1;
		}
		i++;
		j++;
	}
	return -1;
}

char *GetAnimNameByIndex(short index)
{
	if(index > 1812 || index < 1) return NULL;

	return szAnimNames[index];
}

void InitAnimNameHashes()
{
	char szAnimLib[32];
	char szAnimName[32];

	int x = 0;
	while(x != 1812) {
		memset(szAnimLib,0,sizeof(szAnimLib));
		memset(szAnimName,0,sizeof(szAnimName));

		char *szSeparator = strchr(szAnimNames[x], ':');
		if(szSeparator != NULL) {
			strncpy(szAnimLib, szAnimNames[x], szSeparator - szAnimNames[x]);
			strcpy(szAnimName, szSeparator + 1);

			dwAnimNameHashes[x] = CRC32FromUpcaseString(szAnimName);
		}
		x++;
	}
}

short GetAnimIndexByHash(DWORD dwAnimHash)
{
	short x = 0;

	while(x != 1812) {
		if(dwAnimNameHashes[x] == dwAnimHash) {
			return x;
		}
		x++;
	}
	return -1;
}
